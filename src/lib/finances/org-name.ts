// Сокращение организационно-правовой формы в названии контрагента для ПЛОТНЫХ
// UI-списков, чтобы в строке было видно само название, а не «ОБЩЕСТВО С …».
// В документах/выгрузках (1C, выписка, платёжка) используется ПОЛНОЕ юр-название.

// Порядок важен: специфичные формы — раньше общих ("ПУБЛИЧНОЕ АКЦИОНЕРНОЕ
// ОБЩЕСТВО" до "АКЦИОНЕРНОЕ ОБЩЕСТВО").
const FORMS: ReadonlyArray<readonly [RegExp, string]> = [
  [/обществ[оа]\s+с\s+ограниченной\s+ответственн?ость?ю/giu, "ООО"],
  [/публичное\s+акционерное\s+общество/giu, "ПАО"],
  [/непубличное\s+акционерное\s+общество/giu, "АО"],
  [/закрытое\s+акционерное\s+общество/giu, "ЗАО"],
  [/открытое\s+акционерное\s+общество/giu, "ОАО"],
  [/акционерное\s+общество/giu, "АО"],
  [/индивидуальн[а-яё]*\s+предпринимател[а-яё]*/giu, "ИП"],
  [/товарищество\s+с\s+ограниченной\s+ответственностью/giu, "ТОО"],
  [/государственное\s+унитарное\s+предприятие/giu, "ГУП"],
  [/муниципальное\s+унитарное\s+предприятие/giu, "МУП"],
];

/**
 * Сокращает орг-форму в начале названия: «ОБЩЕСТВО С ОГРАНИЧЕННОЙ
 * ОТВЕТСТВЕННОСТЬЮ "РОМАШКА"» → «ООО "РОМАШКА"». Уже сокращённые названия
 * («ООО «РНС»») не меняются. Возвращает null как есть.
 */
export function abbreviateOrgName(name: string | null | undefined): string | null {
  if (!name || !name.trim()) return null;
  let result = name;
  for (const [pattern, abbr] of FORMS) {
    if (pattern.test(result)) {
      result = result.replace(pattern, abbr);
      break; // одна форма на название
    }
  }
  // схлопнуть лишние пробелы, убрать пробел перед закрывающей кавычкой/запятой
  return result.replace(/\s{2,}/g, " ").replace(/\s+([»,])/g, "$1").trim();
}
